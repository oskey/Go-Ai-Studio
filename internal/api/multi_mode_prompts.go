func buildMultiConstructScenePromptInstruction(charCount int) string {
	_ = charCount
	return `当前是双角色镜头：
- 最多只允许这 2 名主要角色作为参考角色。
- 必须优先使用导演化构图，例如过肩镜头、正反打、前景背身配远景正脸、双人侧身对峙、半身特写配背景虚化。
- 若还需要第三方人物，只能把他们作为未命名背景人物写进场景描述，不得当成参考角色。
- 必须严格根据当前场景基础描述里已经建立的前后、左右、远近、朝向、遮挡与视觉主次关系复现两名角色，不得自行洗掉人物占位。
- 两名角色必须都真实出现在画面中，不允许只生成其中一人、弱化其中一人到几乎不可辨认，或把两张参考图平均成相似五官与相似脸型。
- 两名角色都必须分别具备足够的可见锚点：至少尽量覆盖脸部识别点、体态轮廓、固定服装结构/主副配色、稳定装备位置中的大部分内容。不要让第二名角色只剩“听者、陪体、对面那人”这种弱身份。
- 两名角色都应优先只保留当前镜头真正可见、最能识别身份的少量强锚点，不要把全部稳定装备和所有服装层次一次性写成长清单；当前构图里看不见的稳定装备不要硬写。
- 若两名角色存在明显性别差异、年龄差异、肤色差异、脸型差异、身高差或体型差，你必须把这些差异直接翻译成可见画面：例如男性更宽的肩背与更平直的胸廓、女性更清晰的锁骨与胸腰线、更明确的女性面部轮廓与五官柔硬差异。不要把男女都画成同一张中性平均脸。
- 若当前场景基础描述已说明两人互相朝向、彼此对望、左朝右或右朝左，你必须让两人身体和脸部朝向对方，优先使用三分之四侧脸、半侧脸、侧身、前景肩背遮挡等构图；不要把他们退化成左右各站一人并同时正对镜头的平面摆拍。`
}

func buildMultiVideoFingerprintRefineAddendum() string {
	return `
17. 如果当前镜头里有 2 名正式角色，你必须把“双人镜头调度”当成核心任务之一：
   - 若当前镜头里有 2 名正式角色，你必须明确谁是当前说话者、谁是听者、谁更靠前、谁更靠后、谁在左右、谁是当前视觉主位。
   - 若其中一人说话，其他人必须保持闭口，只允许有听的微动作，例如视线停留、肩膀轻动、呼吸起伏、轻微耸肩或头部极小角度变化。
   - 只要当前镜头有对白，当前主说话者就必须同时是当前最可读的口型承载位；优先让说话者占据清晰正脸、三分之四侧脸或清晰半侧脸主位。若听者是更大的清晰正脸，而说话者只在前景肩背、边缘半脸、背侧、虚焦层或过小比例层，你必须把它视为上游 coverage 错误并要求改机位或拆镜，不能把口型错绑到听者脸上。
   - 若某人当前以背对镜头、侧背、过肩或半遮挡方式入画，你必须继续保持这种朝向，不要让 LTX2.3 自行把他转成正脸。
   - 双人镜头默认不要让两个人同时都正对镜头；优先使用三分之四侧脸、侧身、过肩、半轮廓、前景肩背遮挡等更稳的朝向，让模型不必补完整另一半脸。
   - 双人面对面对话时，优先保持头部朝向稳定，不要让角色在镜头中途大幅转脸去暴露原本看不见的另一半脸；如果剧情确实要求转动脸部，镜头应在转动瞬间结束，或顺势转入背面/侧背视角，而不是让模型自行补完整正脸。
   - 若当前镜头是稳定的双人面对面对话、two-shot 或 over-the-shoulder，且两人不需要明显转脸，可允许适度延长镜头，由 LTX2.3 持续承载一小段对白；但必须明确指定谁开口、谁闭口、谁是主焦点。
   - 若角色开始做事或触碰关键道具，不要继续把它留在同一段对话镜头里一笔带过；应在 phases 中体现导演式切换思路：先保留起势，再把镜头重心落到手部、武器、道具、脚步或动作结果，形成 insert shot / cut-in 的节奏感。
   - 若对白是快速来回交锋或短句连发，你可以在同一镜头内通过主焦点轻微偏移来表现当前说话者，但更稳的做法仍是把这种快速交换拆成更细的对向镜头、听者反应镜头或短 insert shot，让节奏更像可剪辑的 shot-reverse-shot。
   - 不要把双人对话写成两个人都在抢镜或同时开口；要像导演一样指定当前镜头到底在对准谁。
   - 若两名角色本身在性别、年龄、身高、体型或身份上有明显差异，视频提示词也必须继续保留这些差异，不要把二人的脸、肩背、身形轮廓和说话状态写得越来越相似，避免视频后续滑向同脸化或中性化。
   - 连续双人镜头必须保持稳定的屏幕方向和视线轴线；A 不要在上一段还位于画面左侧、下一段无过渡地跳去右侧。若需要换轴，应通过中性镜头或明显切换过渡。
   - 过肩视频镜头中，前景听者只保持边缘肩背、后脑、耳侧、袖口或武器边缘的小幅微动，不要让前景角色在镜头中途自己转出大面积清晰正脸。
   - 若当前段落使用 shot-reverse-shot，前后镜头必须体现清楚的角度差、景别差或焦点层次差，避免两个镜头像左右翻转的同一张图。
   - 左右分屏只在明确适合时使用：例如电话、异地对话、并行反应、强对照或需要同时看到双方表情时。若使用分屏，必须明确左屏是谁、右屏是谁、双方是否同时说话、双方朝向屏幕内侧还是外侧，并保持两侧人物身份稳定；不要把普通同地对话无意义地做成分屏。
   - 你可以根据镜头任务使用 two-shot、dirty single、profile two-shot、同侧并肩望向同一方向、俯拍建立镜头、高角度压迫镜头、顶视位置关系镜头、道具 insert shot、听者反应 cutaway 等稳定模板；但每一段都必须让 LTX2.3 清楚知道主位是谁、谁闭口、谁只做陪体微动。
   - 若当前镜头是俯拍、高角度或顶视，视频提示词必须继续遵守该视角：人物只做肩背、头顶、手臂、脚步、披风、发尾等可见部位的微动，不要让人物突然抬头正对镜头或在该角度下暴露原本看不见的完整脸。
   - 若当前镜头是同侧并行或共同望向同一方向，不要强行让两人中途转头互看；优先保持共同朝向，只让当前说话者承担口型，另一人以呼吸、视线、肩部或衣摆微动作为回应。
   - 双参考角色镜头的视频阶段只继承大纲和场景阶段已经成立的镜头模板与对白长度判断；如果一段对白把真实中文语速、标点停顿、换气、情绪留白和必要听者反应都算进去后自然总长会超过 12 秒，正确做法是上游大纲拆镜，不是在视频阶段压缩语速、截掉尾句或盲目兜底。`
}

func buildScenePromptModeInstruction(projectSceneMode int, charCount int, disableReferenceImages bool) string {
	if charCount == 0 {
		return `当前是空镜或纯场景镜头：
- 不要强行加入命名角色。
- 必须把环境、建筑、器物、气候、光线、空间纵深写满。
- 如果场景需要路人或背景生物，也只能作为未命名背景元素写进提示词。`
	}
	if charCount == 1 {
		return `当前是单角色镜头：
- 重点表现该角色的表情、动作、姿态、服装状态与临时变化。
- 如果这是连续对话中的一个镜头，必须默认它和前后镜头处于同一空间，只是机位或景别有变化。
- 必须严格继承场景基础描述里已经给出的站位、视线方向、景别与遮挡关系。`
	}
	if disableReferenceImages {
		return buildPromptOnlyConstructScenePromptInstruction(charCount)
	}
	return buildMultiConstructScenePromptInstruction(charCount)
}
