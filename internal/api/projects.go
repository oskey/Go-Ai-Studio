func focusSceneTextOnGroup(text string, group []string, characters []GeneratedCharacter, separator string) string {
	trimmed := strings.TrimSpace(text)
	if trimmed == "" || len(group) == 0 {
		return trimmed
	}
	allowed := make(map[string]struct{}, len(group))
	for _, name := range group {
		allowed[name] = struct{}{}
	}
	disallowed := make([]string, 0, len(characters))
	for _, char := range characters {
		name := normalizeGeneratedCharacterName(char.Name)
		if name == "" {
			continue
		}
		if _, ok := allowed[name]; ok {
			continue
		}
		disallowed = append(disallowed, name)
	}
	if len(disallowed) == 0 {
		return trimmed
	}
	parts := strings.Split(trimmed, separator)
	kept := make([]string, 0, len(parts))
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if part == "" {
			continue
		}
		hasDisallowed := false
		for _, name := range disallowed {
			if strings.Contains(part, name) {
				hasDisallowed = true
				break
			}
		}
		if hasDisallowed {
			continue
		}
		kept = append(kept, part)
	}
	if len(kept) == 0 {
		return trimmed
	}
	joined := strings.Join(kept, separator)
	if separator == "。" && !strings.HasSuffix(joined, "。") {
		joined += "。"
	}
	return joined
}
