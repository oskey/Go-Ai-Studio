func upsertEpisodeEditorialGuide(tx *gorm.DB, projectID uint, episode int, episodeSummary string, editingNotes string, detailedEditingNotes string) error {
	if projectID == 0 || episode <= 0 {
		return nil
	}
	if strings.TrimSpace(episodeSummary) == "" && strings.TrimSpace(editingNotes) == "" && strings.TrimSpace(detailedEditingNotes) == "" {
		return nil
	}
	updates := map[string]interface{}{
		"episode_summary":        strings.TrimSpace(episodeSummary),
		"editing_notes":          strings.TrimSpace(editingNotes),
		"detailed_editing_notes": strings.TrimSpace(detailedEditingNotes),
		"updated_at":             time.Now(),
	}
	var guide models.EpisodeEditorialGuide
	err := tx.Where("project_id = ? AND episode = ?", projectID, episode).First(&guide).Error
	if err != nil {
		if err != gorm.ErrRecordNotFound {
			return err
		}
		guide = models.EpisodeEditorialGuide{
			ProjectID:            projectID,
			Episode:              episode,
			EpisodeSummary:       strings.TrimSpace(episodeSummary),
			EditingNotes:         strings.TrimSpace(editingNotes),
			DetailedEditingNotes: strings.TrimSpace(detailedEditingNotes),
		}
		return tx.Create(&guide).Error
	}
	return tx.Model(&guide).Updates(updates).Error
}
func normalizeProjectAnchorMemoryItems(items []ProjectAnchorMemoryItem) []ProjectAnchorMemoryItem {
	if len(items) == 0 {
		return nil
	}
	filtered := make([]ProjectAnchorMemoryItem, 0, len(items))
	seen := make(map[string]struct{}, len(items))
	for _, item := range items {
		anchorType := strings.TrimSpace(strings.ToLower(item.AnchorType))
		if anchorType != "location" && anchorType != "prop" && anchorType != "vehicle" {
			continue
		}
		anchorKey := strings.TrimSpace(item.AnchorKey)
		summary := strings.TrimSpace(item.Summary)
		if anchorKey == "" || summary == "" {
			continue
		}
		identity := anchorType + "::" + strings.ToLower(anchorKey)
		if _, exists := seen[identity]; exists {
			continue
		}
		seen[identity] = struct{}{}
		filtered = append(filtered, ProjectAnchorMemoryItem{
			AnchorType: anchorType,
			AnchorKey:  anchorKey,
			Summary:    summary,
		})
	}
	return filtered
}

func normalizeProjectAnchorMemoryArrayItems(items []interface{}) []ProjectAnchorMemoryItem {
	if len(items) == 0 {
		return nil
	}
	result := make([]ProjectAnchorMemoryItem, 0, len(items))
	for _, item := range items {
		anchorMap, ok := item.(map[string]interface{})
		if !ok {
			continue
		}
		result = append(result, ProjectAnchorMemoryItem{
			AnchorType: readMapString(anchorMap, "anchor_type"),
			AnchorKey:  firstNonEmptyString(readMapString(anchorMap, "anchor_key"), readMapString(anchorMap, "key")),
			Summary:    firstNonEmptyString(readMapString(anchorMap, "summary"), readMapString(anchorMap, "content")),
		})
	}
	return normalizeProjectAnchorMemoryItems(result)
}

func mergeProjectAnchorMemoryItems(existing []ProjectAnchorMemoryItem, updates []ProjectAnchorMemoryItem) []ProjectAnchorMemoryItem {
	if len(existing) == 0 {
		return normalizeProjectAnchorMemoryItems(updates)
	}
	merged := normalizeProjectAnchorMemoryItems(existing)
	indexByIdentity := make(map[string]int, len(merged))
	for idx, item := range merged {
		identity := item.AnchorType + "::" + strings.ToLower(item.AnchorKey)
		indexByIdentity[identity] = idx
	}
	for _, item := range normalizeProjectAnchorMemoryItems(updates) {
		identity := item.AnchorType + "::" + strings.ToLower(item.AnchorKey)
		if idx, ok := indexByIdentity[identity]; ok {
			merged[idx] = item
			continue
		}
		indexByIdentity[identity] = len(merged)
		merged = append(merged, item)
	}
	return merged
}

