func upsertProjectAnchorMemories(tx *gorm.DB, projectID uint, episode int, anchors []ProjectAnchorMemoryItem, overwrite bool) error {
	if projectID == 0 {
		return nil
	}
	if overwrite && len(anchors) > 0 {
		if err := tx.Where("project_id = ?", projectID).Delete(&models.ProjectAnchorMemory{}).Error; err != nil {
			return err
		}
	}
	for _, anchor := range anchors {
		var existing models.ProjectAnchorMemory
		err := tx.Where("project_id = ? AND anchor_type = ? AND anchor_key = ?", projectID, anchor.AnchorType, anchor.AnchorKey).First(&existing).Error
		if err != nil {
			if err != gorm.ErrRecordNotFound {
				return err
			}
			newAnchor := models.ProjectAnchorMemory{
				ProjectID:    projectID,
				AnchorType:   anchor.AnchorType,
				AnchorKey:    anchor.AnchorKey,
				Summary:      anchor.Summary,
				EpisodeRefs:  formatEpisodeRefs([]int{episode}),
				FirstEpisode: episode,
				LastEpisode:  episode,
			}
			if err := tx.Create(&newAnchor).Error; err != nil {
				return err
			}
			continue
		}
		updatedEpisodeRefs, firstEpisode, lastEpisode := addEpisodeRef(existing.EpisodeRefs, episode)
		updates := map[string]interface{}{
			"summary":       anchor.Summary,
			"episode_refs":  updatedEpisodeRefs,
			"first_episode": firstEpisode,
			"last_episode":  lastEpisode,
			"updated_at":    time.Now(),
		}
		if err := tx.Model(&existing).Updates(updates).Error; err != nil {
			return err
		}
	}
	return nil
}
