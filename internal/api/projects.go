func extractCompletedJSONObjectItemsForArray(jsonContent string, arrayKey string) []string {
	keyIdx := strings.Index(jsonContent, `"`+arrayKey+`"`)
	if keyIdx < 0 {
		return nil
	}
	arrayStart := strings.Index(jsonContent[keyIdx:], "[")
	if arrayStart < 0 {
		return nil
	}
	start := keyIdx + arrayStart
	items := make([]string, 0)
	inString := false
	escape := false
	objectDepth := 0
	itemStart := -1
	for i := start + 1; i < len(jsonContent); i++ {
		ch := jsonContent[i]
		if inString {
			if escape {
				escape = false
				continue
			}
			if ch == '\\' {
				escape = true
				continue
			}
			if ch == '"' {
				inString = false
			}
			continue
		}
		switch ch {
		case '"':
			inString = true
		case '{':
			if objectDepth == 0 {
				itemStart = i
			}
			objectDepth++
		case '}':
			if objectDepth == 0 {
				continue
			}
			objectDepth--
			if objectDepth == 0 && itemStart >= 0 {
				items = append(items, strings.TrimSpace(jsonContent[itemStart:i+1]))
				itemStart = -1
			}
		case ']':
			if objectDepth == 0 {
				return items
			}
		}
	}
	return items
}
