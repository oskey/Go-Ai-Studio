func ExportStoryJSON(c *gin.Context) {
	projectID := parseUint(c.Param("id"))
	if projectID == 0 {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid project id"})
		return
	}

	var req ExportStoryJSONPayload
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	if req.Episode <= 0 {
		c.JSON(http.StatusBadRequest, gin.H{"error": "episode must be greater than 0"})
		return
	}

	var project models.Project
	if err := db.DB.First(&project, projectID).Error; err != nil {
		c.JSON(http.StatusNotFound, gin.H{"error": "Project not found"})
		return
	}

	var shots []models.Shot
	if err := db.DB.Where("project_id = ? AND episode = ?", projectID, req.Episode).
		Order("scene_number asc").
		Find(&shots).Error; err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to load shots"})
		return
	}
	if len(shots) == 0 {
		c.JSON(http.StatusNotFound, gin.H{"error": fmt.Sprintf("episode %d has no shots", req.Episode)})
		return
	}

	episodeMemory, err := loadEpisodeMemory(projectID, req.Episode)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}

	var allCharacters []models.Character
	if err := db.DB.Where("project_id = ?", projectID).Order("id asc").Find(&allCharacters).Error; err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to load characters"})
		return
	}

	statusNames := make(map[string]struct{}, len(episodeMemory.CharacterStatus))
	for _, item := range episodeMemory.CharacterStatus {
		name := strings.TrimSpace(item.Name)
		if name == "" {
			continue
		}
		statusNames[name] = struct{}{}
	}

	exportCharacters := make([]lightweightStoryCharacter, 0)
	for _, record := range allCharacters {
		normalized := normalizeStoryCharacterRecord(record)
		if normalized.Name == "" {
			continue
		}
		if len(statusNames) > 0 {
			if _, ok := statusNames[normalized.Name]; !ok {
				continue
			}
		}
		exportCharacters = append(exportCharacters, normalized)
	}
	if len(exportCharacters) == 0 {
		for _, record := range allCharacters {
			normalized := normalizeStoryCharacterRecord(record)
			if normalized.Name == "" {
				continue
			}
			exportCharacters = append(exportCharacters, normalized)
		}
	}

	exportScenes := make([]lightweightStoryScene, 0, len(shots))
	for _, shot := range shots {
		sceneID := shot.SceneID
		if sceneID <= 0 {
			sceneID = shot.SceneNumber
		}
		exportScenes = append(exportScenes, lightweightStoryScene{
			SceneID:         sceneID,
			DurationSeconds: shot.DurationSeconds,
			Narration:       strings.TrimSpace(shot.Narration),
			ImagePrompt:     strings.TrimSpace(shot.ImagePrompt),
			VideoPrompt:     strings.TrimSpace(shot.VideoPrompt),
		})
	}

	payload := lightweightStoryResponse{
		TotalScenes:   len(exportScenes),
		Characters:    exportCharacters,
		Scenes:        exportScenes,
		EpisodeMemory: episodeMemory,
	}

	exportBytes, err := json.MarshalIndent(payload, "", "  ")
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to encode export payload"})
		return
	}

	filename := fmt.Sprintf("%s_episode_%d_story.json", strings.TrimSpace(project.Code), req.Episode)
	c.Header("Content-Type", "application/json; charset=utf-8")
	c.Header("Content-Disposition", fmt.Sprintf("attachment; filename=\"%s\"", filename))
	c.Data(http.StatusOK, "application/json; charset=utf-8", exportBytes)
}

