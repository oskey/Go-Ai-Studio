func normalizeEpisodeSceneMemoryType(raw string) string {
	trimmed := strings.ToLower(strings.TrimSpace(raw))
	switch {
	case trimmed == "", trimmed == "none":
		return ""
	case trimmed == "space", strings.Contains(trimmed, "空间"), strings.Contains(trimmed, "场景空间"):
		return "space"
	case trimmed == "prop", strings.Contains(trimmed, "道具"), strings.Contains(trimmed, "物件"):
		return "prop"
	case trimmed == "appearance", strings.Contains(trimmed, "外观"), strings.Contains(trimmed, "发型"), strings.Contains(trimmed, "脸部"):
		return "appearance"
	case trimmed == "outfit", strings.Contains(trimmed, "服装"), strings.Contains(trimmed, "衣装"), strings.Contains(trimmed, "穿着"):
		return "outfit"
	case trimmed == "blocking", strings.Contains(trimmed, "站位"), strings.Contains(trimmed, "座位"), strings.Contains(trimmed, "走位"), strings.Contains(trimmed, "位置关系"):
		return "blocking"
	default:
		return trimmed
	}
}
func episodeSceneMemoryTypeLabel(memoryType string) string {
	switch normalizeEpisodeSceneMemoryType(memoryType) {
	case "space":
		return "空间结构"
	case "prop":
		return "关键道具"
	case "appearance":
		return "人物外观"
	case "outfit":
		return "当前服装"
	case "blocking":
		return "站位与桌位"
	default:
		return strings.TrimSpace(memoryType)
	}
}
