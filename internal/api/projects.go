func normalizeStringArray(values ...interface{}) []string {
	result := make([]string, 0)
	for _, value := range values {
		switch typed := value.(type) {
		case []interface{}:
			for _, item := range typed {
				switch inner := item.(type) {
				case string:
					trimmed := strings.TrimSpace(inner)
					if trimmed != "" {
						result = append(result, trimmed)
					}
				case map[string]interface{}:
					role := firstNonEmptyString(readMapString(inner, "role"), readMapString(inner, "speaker"))
					content := firstNonEmptyString(readMapString(inner, "content"), readMapString(inner, "text"))
					if role != "" && content != "" {
						result = append(result, role+"："+content)
					}
				}
			}
		}
		if len(result) > 0 {
			return result
		}
	}
	return result
}
