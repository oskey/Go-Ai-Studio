func cleanupLLMJSON(content string) string {
	jsonContent := strings.TrimSpace(content)
	re := regexp.MustCompile("(?s)```(?:json)?\\s*(.*?)\\s*```")
	match := re.FindStringSubmatch(jsonContent)
	if len(match) > 1 {
		jsonContent = match[1]
	}
	arrayStart := strings.Index(jsonContent, "[")
	arrayEnd := strings.LastIndex(jsonContent, "]")
	objectStart := strings.Index(jsonContent, "{")
	objectEnd := strings.LastIndex(jsonContent, "}")
	if arrayStart >= 0 && arrayEnd >= arrayStart && (objectStart == -1 || arrayStart < objectStart) {
		jsonContent = jsonContent[arrayStart : arrayEnd+1]
	} else if objectStart >= 0 && objectEnd >= objectStart {
		jsonContent = jsonContent[objectStart : objectEnd+1]
	}
	jsonContent = normalizeLLMJSONTypography(jsonContent)
	jsonContent = repairCommonLLMJSONIssues(jsonContent)
	jsonContent = repairMalformedLLMJSON(jsonContent)
	return strings.TrimSpace(jsonContent)
}

func normalizeLLMJSONTypography(input string) string {
	replacer := strings.NewReplacer(
		"\ufeff", "",
		"“", `"`,
		"”", `"`,
		"„", `"`,
		"‟", `"`,
		"＂", `"`,
		"‘", `'`,
		"’", `'`,
		"‚", `'`,
		"‛", `'`,
		"`", `"`,
	)
	return replacer.Replace(input)
}

func repairCommonLLMJSONIssues(input string) string {
	fixed := input
	// Repair scene objects where the model emits:
	// "scene_number": 18,
	// "场景名",
	// instead of:
	// "scene_number": 18,
	// "name": "场景名",
	sceneNameRe := regexp.MustCompile(`("scene_number"\s*:\s*\d+\s*,\s*)"([^"\r\n]+)"\s*,\s*("description"\s*:)`)
	fixed = sceneNameRe.ReplaceAllString(fixed, `${1}"name": "$2", $3`)
	return fixed
}

func repairMalformedLLMJSON(input string) string {
	fixed := strings.TrimSpace(input)
	if fixed == "" {
		return fixed
	}
	fixed = escapeLooseJSONStringContent(fixed)
	fixed = stripInvalidJSONRunesOutsideStrings(fixed)
	fixed = repairMissingJSONCommas(fixed)
	fixed = repairTrailingJSONCommas(fixed)
	fixed = repairMissingJSONCommas(fixed)
	return strings.TrimSpace(fixed)
}

func escapeLooseJSONStringContent(input string) string {
	runes := []rune(input)
	var builder strings.Builder
	builder.Grow(len(input) + 16)

	inString := false
	escapeNext := false

	for i, r := range runes {
		if inString {
			if escapeNext {
				builder.WriteRune(r)
				escapeNext = false
				continue
			}
			switch r {
			case '\\':
				builder.WriteRune(r)
				escapeNext = true
			case '"':
				if looseJSONStringQuoteTerminates(runes, i) {
					builder.WriteRune(r)
					inString = false
				} else {
					builder.WriteString(`\"`)
				}
			case '\n':
				builder.WriteString(`\n`)
			case '\r':
				// Skip CR and let LF or subsequent content carry on.
			case '\t':
				builder.WriteString(`\t`)
			default:
				if unicode.IsControl(r) {
					continue
				}
				builder.WriteRune(r)
			}
			continue
		}

		if r == '"' {
			inString = true
		}
		builder.WriteRune(r)
	}

	return builder.String()
}

func looseJSONStringQuoteTerminates(runes []rune, quoteIndex int) bool {
	for i := quoteIndex + 1; i < len(runes); i++ {
		switch runes[i] {
		case ' ', '\n', '\r', '\t':
			continue
		case ',', '}', ']', ':':
			return true
		default:
			return false
		}
	}
	return true
}

func stripInvalidJSONRunesOutsideStrings(input string) string {
	var builder strings.Builder
	builder.Grow(len(input))

	inString := false
	escapeNext := false

	for _, r := range input {
		if r == utf8.RuneError {
			continue
		}
		if inString {
			if escapeNext {
				builder.WriteRune(r)
				escapeNext = false
				continue
			}
			switch r {
			case '\\':
				builder.WriteRune(r)
				escapeNext = true
			case '"':
				builder.WriteRune(r)
				inString = false
			default:
				if unicode.IsControl(r) {
					continue
				}
				builder.WriteRune(r)
			}
			continue
		}

		switch {
		case r == '"':
			builder.WriteRune(r)
			inString = true
		case r == '：':
			builder.WriteRune(':')
		case r == '，':
			builder.WriteRune(',')
		case unicode.IsSpace(r):
			builder.WriteRune(r)
		case strings.ContainsRune(`{}[]:,-+.0123456789tfruealsnE`, r):
			builder.WriteRune(r)
		}
	}

	return builder.String()
}

func repairMissingJSONCommas(input string) string {
	fixed := input
	patterns := []struct {
		re   *regexp.Regexp
		repl string
	}{
		{
			re:   regexp.MustCompile(`((?:"(?:\\.|[^"\\])*"|true|false|null|-?\d+(?:\.\d+)?(?:[eE][+\-]?\d+)?|\}|\]))\s*("(?:(?:\\.|[^"\\])*)"\s*:)`),
			repl: `$1, $2`,
		},
		{
			re:   regexp.MustCompile(`(\}|\])\s*(\{|\[)`),
			repl: `$1, $2`,
		},
	}
	for _, pattern := range patterns {
		fixed = pattern.re.ReplaceAllString(fixed, pattern.repl)
	}
	return fixed
}

func repairTrailingJSONCommas(input string) string {
	re := regexp.MustCompile(`,(\s*[}\]])`)
	return re.ReplaceAllString(input, `$1`)
}

