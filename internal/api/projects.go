// AddProject adds a new project
func AddProject(c *gin.Context) {
	var payload struct {
		Name                   string `json:"name"`
		Code                   string `json:"code"`
		ArtStyleID             uint   `json:"art_style_id"`
		Description            string `json:"description"`
		DisableReferenceImages *bool  `json:"disable_reference_images"`
	}
	if err := c.ShouldBindJSON(&payload); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	project := models.Project{
		Name:                   payload.Name,
		Code:                   payload.Code,
		ArtStyleID:             payload.ArtStyleID,
		Description:            payload.Description,
		SceneMode:              1,
		DisableReferenceImages: true,
		CreatedAt:              time.Now(),
		UpdatedAt:              time.Now(),
	}
	// Validate Code (English only, alphanumeric + underscore/hyphen)
	matched, _ := regexp.MatchString(`^[a-zA-Z0-9_-]+$`, project.Code)
	if !matched {
		c.JSON(http.StatusBadRequest, gin.H{"error": "项目编号只允许输入英文、数字、下划线或连字符"})
		return
	}
	// Check if Code exists
	var count int64
	db.DB.Model(&models.Project{}).Where("code = ?", project.Code).Count(&count)
	if count > 0 {
		c.JSON(http.StatusBadRequest, gin.H{"error": "项目编号已存在"})
		return
	}
	// Create project directory in output folder
	// Assuming output dir is relative to working directory for now
	outputDir := "output"
	projectDir := filepath.Join(outputDir, project.Code)
	if err := os.MkdirAll(projectDir, 0755); err != nil {
		Log(LogLevelError, "创建项目目录失败", err.Error())
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to create project directory"})
		return
	}
	if err := db.DB.Create(&project).Error; err != nil {
		Log(LogLevelError, "创建项目失败", err.Error())
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to create project"})
		return
	}
	Log(LogLevelInfo, "创建项目", fmt.Sprintf("创建了新项目: %s (%s)", project.Name, project.Code))
	// Reload to get ArtStyle
	db.DB.Preload("ArtStyle").First(&project, project.ID)
	c.JSON(http.StatusCreated, project)
}

