func buildEpisodeSceneContext(scenes []GeneratedScene, emptyText string) string {
	if len(scenes) == 0 {
		return emptyText
	}
	parts := make([]string, 0, len(scenes))
	for _, scene := range scenes {
		roleText := strings.Join(scene.Characters, "、")
		if strings.TrimSpace(roleText) == "" {
			roleText = "无正式角色主位"
		}
		locationText := strings.TrimSpace(scene.LocationID)
		if locationText == "" {
			locationText = "未记录"
		}
		parts = append(parts, fmt.Sprintf("第%d集第%d场《%s》：location_id=%s｜场景目标=%s｜场景描述=%s｜角色=%s", scene.Episode, scene.SceneNumber, firstNonEmptyString(scene.SceneName, scene.Name), locationText, strings.TrimSpace(scene.SceneGoal), strings.TrimSpace(scene.Description), roleText))
	}
	return strings.Join(parts, "\n")
}
