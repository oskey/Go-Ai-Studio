func classifySceneBatchValidationError(err error) (string, string) {
	if err == nil {
		return "规则", "因场景规则校验失败"
	}
	errText := err.Error()
	switch {
	case strings.Contains(errText, "is missing valid total_duration_seconds"),
		strings.Contains(errText, "exceeds single-shot limit"):
		return "时长", "因视频总长度校验失败"
	case strings.Contains(errText, "forbidden continuity phrase"),
		strings.Contains(errText, "context-dependent references"):
		return "连续性", "因依赖前镜表述失败"
	case strings.Contains(errText, "uses ambiguous character shorthand"):
		return "角色名", "因角色名称不一致失败"
	case strings.Contains(errText, "missing from characters"),
		strings.Contains(errText, "characters order does not match visible priority"):
		return "角色绑定", "因角色绑定校验失败"
	default:
		return "规则", "因场景规则校验失败"
	}
}

