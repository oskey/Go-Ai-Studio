func sanitizeFaceFingerprint(faceFingerprint string, description string, fingerprint string) string {
	trimmed := strings.TrimSpace(faceFingerprint)
	if trimmed != "" {
		return trimmed
	}
	base := extractFaceFingerprint(description)
	if base != "" {
		return base
	}
	return extractFaceFingerprint(fingerprint)
}
func extractFaceFingerprint(input string) string {
	cleaned := strings.TrimSpace(input)
	if cleaned == "" {
		return ""
	}
	keywords := []string{
		"脸", "面容", "五官", "眉", "眼", "眼神", "鼻", "鼻梁", "唇", "嘴唇", "肤色", "皮肤",
		"脸型", "瓜子脸", "国字脸", "发际线", "发色", "短发", "长发", "马尾", "发髻",
	}
	seps := []string{"。", "，", "、", ",", ";", "；"}
	normalized := cleaned
	for _, sep := range seps[1:] {
		normalized = strings.ReplaceAll(normalized, sep, seps[0])
	}
	parts := strings.Split(normalized, seps[0])
	kept := make([]string, 0, len(parts))
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if part == "" {
			continue
		}
		for _, keyword := range keywords {
			if strings.Contains(part, keyword) {
				kept = append(kept, part)
				break
			}
		}
		if len(kept) >= 8 {
			break
		}
	}
	return strings.Join(kept, "，")
}
func buildCharacterMemory(chars []models.Character) []CharacterMemory {
	memories := make([]CharacterMemory, 0, len(chars))
	indexByName := make(map[string]int, len(chars))
	for _, char := range chars {
		normalizedName := normalizeGeneratedCharacterName(char.Name)
		if normalizedName == "" {
			continue
		}
		memory := CharacterMemory{
			Name:            normalizedName,
			FaceFingerprint: sanitizeFaceFingerprint(char.FaceFingerprint, char.Description, char.Fingerprint),
			Description:     char.Description,
			Fingerprint:     char.Fingerprint,
		}
		if idx, ok := indexByName[normalizedName]; ok {
			memories[idx] = memory
			continue
		}
		indexByName[normalizedName] = len(memories)
		memories = append(memories, memory)
	}
	return memories
}
