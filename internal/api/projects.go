func loadProjectRecordCounts[T any](projectIDs []uint) map[uint]int64 {
	counts := make(map[uint]int64, len(projectIDs))
	if len(projectIDs) == 0 {
		return counts
	}
	type countRow struct {
		ProjectID uint
		Count     int64
	}
	var rows []countRow
	if err := db.DB.Model(new(T)).
		Select("project_id, COUNT(*) AS count").
		Where("project_id IN ?", projectIDs).
		Group("project_id").
		Scan(&rows).Error; err != nil {
		return counts
	}
	for _, row := range rows {
		counts[row.ProjectID] = row.Count
	}
	return counts
}
