func mergeGeneratedCharacterSets(existing []GeneratedCharacter, current []GeneratedCharacter) []GeneratedCharacter {
	merged := make([]GeneratedCharacter, 0, len(existing)+len(current))
	indexByName := make(map[string]int, len(existing)+len(current))
	for _, char := range existing {
		name := strings.TrimSpace(char.Name)
		if name == "" {
			continue
		}
		indexByName[name] = len(merged)
		merged = append(merged, char)
	}
	for _, char := range current {
		name := strings.TrimSpace(char.Name)
		if name == "" {
			continue
		}
		if idx, ok := indexByName[name]; ok {
			merged[idx] = char
			continue
		}
		indexByName[name] = len(merged)
		merged = append(merged, char)
	}
	return merged
}
