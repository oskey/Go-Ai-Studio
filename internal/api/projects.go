func hydrateGeneratedScenesFromEpisodePlan(scenes []GeneratedScene, plan EpisodePlanResponse) []GeneratedScene {
	if len(scenes) == 0 || len(plan.SceneOutline) == 0 {
		return scenes
	}
	outlineByNumber := make(map[int]EpisodePlanSceneOutlineItem, len(plan.SceneOutline))
	for _, item := range plan.SceneOutline {
		outlineByNumber[item.SceneNumber] = item
	}
	hydrated := make([]GeneratedScene, len(scenes))
	for idx, scene := range scenes {
		scene.normalizeScreenPromptFields()
		outline, ok := outlineByNumber[scene.SceneNumber]
		if !ok {
			hydrated[idx] = scene
			continue
		}
		scene.SceneName = strings.TrimSpace(outline.SceneName)
		scene.Name = scene.SceneName
		scene.LocationID = strings.TrimSpace(outline.LocationID)
		scene.OutlineRef = expectedSceneOutlineRef(scene.SceneNumber)
		scene.SceneGoal = strings.TrimSpace(outline.SceneGoal)
		scene.Characters = append([]string(nil), outline.Characters...)
		scene.CharacterBlocking = buildGeneratedSceneCharacterBlockingFromPlan(plan, outline)
		scene.CameraLock = buildGeneratedSceneCameraLockFromPlan(plan, outline.LocationID)
		hydrated[idx] = scene
	}
	return hydrated
}

