func collectSceneBatchWarnings(scenes []GeneratedScene, characters []GeneratedCharacter, singleMode bool, disableReferenceImages bool) []string {
	_ = singleMode
	_ = disableReferenceImages
	badPhrases := []string{
		"同一空间",
		"同一场景",
		"连续于前镜",
		"连续于前景",
		"上一镜",
		"上一幕",
		"上镜",
		"镜头回到",
		"场景不变",
		"同上",
		"与上镜呈正反打结构",
		"与上镜构成正反打",
		"与前镜呈正反打结构",
		"与前镜构成正反打",
	}
	warnings := make([]string, 0)
	formalCharacterNames := buildFormalCharacterNameSet(characters)
	for _, scene := range scenes {
		if strings.TrimSpace(scene.LocationID) == "" {
			warnings = append(warnings, fmt.Sprintf("scene %d is missing location_id", scene.SceneNumber))
		}
		if strings.TrimSpace(scene.OutlineRef) == "" {
			warnings = append(warnings, fmt.Sprintf("scene %d is missing outline_ref", scene.SceneNumber))
		}
		if strings.TrimSpace(scene.SceneGoal) == "" {
			warnings = append(warnings, fmt.Sprintf("scene %d is missing scene_goal", scene.SceneNumber))
		}
		if strings.TrimSpace(scene.PromptPosScreenZH) != "" && !promptPosScreenHasStrictOrder(scene.PromptPosScreenZH) {
			warnings = append(warnings, fmt.Sprintf("scene %d prompt_pos_screen_zh does not follow strict section order", scene.SceneNumber))
		}
		if strings.TrimSpace(scene.PromptPosZH) == "" {
			warnings = append(warnings, fmt.Sprintf("scene %d is missing localized scene prompts", scene.SceneNumber))
		}
		combined := scene.Description + "\n" + scene.PromptPosScreenZH
		for _, phrase := range badPhrases {
			if strings.Contains(combined, phrase) {
				warnings = append(warnings, fmt.Sprintf("scene %d contains forbidden continuity phrase %q", scene.SceneNumber, phrase))
			}
		}
		promptFields := []string{scene.PromptPosZH, scene.PromptPosScreenZH}
		for name := range formalCharacterNames {
			trimmedName := strings.TrimSpace(name)
			if trimmedName == "" {
				continue
			}
			for _, prompt := range promptFields {
				if strings.Contains(prompt, trimmedName) {
					warnings = append(warnings, fmt.Sprintf("scene %d prompt contains forbidden character name %q", scene.SceneNumber, trimmedName))
				}
			}
		}
		if strings.TrimSpace(scene.VideoFingerprint) != "" {
			payload, err := parseVideoFingerprintPayload(scene.VideoFingerprint)
			if err != nil {
				warnings = append(warnings, fmt.Sprintf("scene %d has invalid video_fingerprint JSON: %v", scene.SceneNumber, err))
			} else {
				if err := validateVideoFingerprintAudioText(payload); err != nil {
					warnings = append(warnings, fmt.Sprintf("scene %d has invalid video_fingerprint audio: %v", scene.SceneNumber, err))
				}
				warnings = append(warnings, collectSceneNarrationWarnings(scene, payload)...)
				if err := validateSceneVideoFingerprintNames(payload, scene); err != nil {
					warnings = append(warnings, err.Error())
				}
			}
		} else {
			warnings = append(warnings, collectSceneNarrationWarnings(scene, nil)...)
		}
	}
	if ambiguous := findAmbiguousSceneCharacterReferences(scenes, characters); len(ambiguous) > 0 {
		warnings = append(warnings, fmt.Sprintf("scene batch uses ambiguous character shorthand: %s", strings.Join(ambiguous, "、")))
	}
	if timingErr := validateSceneBatchTiming(scenes); timingErr != nil {
		warnings = append(warnings, timingErr.Error())
	}
	return warnings
}

func validateMultiSceneFormalCharacterBinding(scene GeneratedScene, characters []GeneratedCharacter, disableReferenceImages bool) error {
	if len(characters) == 0 {
		return nil
	}
	formalCharacterNames := buildFormalCharacterNameSet(characters)
	if len(formalCharacterNames) == 0 {
		return nil
	}
	mentioned := extractMentionedFormalCharacters(strings.TrimSpace(scene.Description+"\n"+scene.PromptPosScreenZH), formalCharacterNames)
	if len(mentioned) == 0 {
		return nil
	}
	maxCharacters := maxSceneCharactersForDialogue(scene.Dialogue, false, disableReferenceImages)
	if len(mentioned) > maxCharacters {
		if disableReferenceImages && hasSceneDialogue(scene.Dialogue) {
			return fmt.Errorf("scene %d contains dialogue and visibly includes more than %d simultaneously clear formal characters in description/prompt, so it must be split before returning", scene.SceneNumber, maxCharacters)
		}
		if disableReferenceImages {
			return fmt.Errorf("scene %d visibly includes more than %d simultaneously clear formal characters in description/prompt and must be split before returning", scene.SceneNumber, maxCharacters)
		}
		return fmt.Errorf("scene %d mentions more than 2 formal characters in visible description/prompt and must be split before returning", scene.SceneNumber)
	}
	bound := make(map[string]struct{}, len(scene.Characters))
	for _, name := range scene.Characters {
		resolvedName := resolveFormalCharacterName(name, characters, formalCharacterNames)
		if resolvedName == "" {
			continue
		}
		bound[resolvedName] = struct{}{}
	}
	missing := make([]string, 0)
	for _, name := range mentioned {
		if _, ok := bound[name]; ok {
			continue
		}
		missing = append(missing, name)
	}
	if len(missing) == 0 {
		return nil
	}
	return fmt.Errorf("scene %d visibly includes formal characters %s in description/prompt but they are missing from characters", scene.SceneNumber, strings.Join(missing, "、"))
}

func validateMultiSceneCharacterOrder(scene GeneratedScene, characters []GeneratedCharacter, disableReferenceImages bool) error {
	if len(scene.Characters) <= 1 || len(characters) == 0 {
		return nil
	}
	formalCharacterNames := buildFormalCharacterNameSet(characters)
	if len(formalCharacterNames) == 0 {
		return nil
	}
	expected := reorderSceneCharactersByVisiblePriority(
		scene.Characters,
		buildGeneratedSceneVisibleCharacterContext(scene, characters),
		formalCharacterNames,
		maxSceneCharactersForDialogue(scene.Dialogue, false, disableReferenceImages),
	)
	if len(expected) != len(scene.Characters) {
		return nil
	}
	for idx := range expected {
		if scene.Characters[idx] != expected[idx] {
			return fmt.Errorf("scene %d characters order does not match visible priority: expected %s", scene.SceneNumber, strings.Join(expected, "、"))
		}
	}
	return nil
}

