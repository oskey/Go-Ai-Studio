func extractMentionedFormalCharacters(text string, formalCharacterNames map[string]struct{}) []string {
	trimmed := strings.TrimSpace(text)
	if trimmed == "" || len(formalCharacterNames) == 0 {
		return nil
	}
	type mentionedCharacter struct {
		name  string
		index int
	}
	mentioned := make([]mentionedCharacter, 0, len(formalCharacterNames))
	for name := range formalCharacterNames {
		idx := strings.Index(trimmed, name)
		if idx < 0 {
			continue
		}
		mentioned = append(mentioned, mentionedCharacter{name: name, index: idx})
	}
	if len(mentioned) == 0 {
		return nil
	}
	for i := 0; i < len(mentioned)-1; i++ {
		for j := i + 1; j < len(mentioned); j++ {
			if mentioned[j].index < mentioned[i].index {
				mentioned[i], mentioned[j] = mentioned[j], mentioned[i]
			}
		}
	}
	result := make([]string, 0, len(mentioned))
	seen := make(map[string]struct{}, len(mentioned))
	for _, item := range mentioned {
		if _, ok := seen[item.name]; ok {
			continue
		}
		seen[item.name] = struct{}{}
		result = append(result, item.name)
	}
	return result
}
