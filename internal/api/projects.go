func sanitizeStableCharacterText(input string, separators []string) string {
	cleaned := strings.TrimSpace(input)
	if cleaned == "" {
		return cleaned
	}
	transientKeywords := []string{
		"受伤", "重伤", "轻伤", "伤口", "鲜血", "流血", "血迹", "沾血", "血污",
		"破损", "撕裂", "撕破", "裂口", "狼狈", "哭泣", "落泪", "泪水", "疲惫",
		"虚弱", "淋雨", "湿身", "打湿", "泥土", "污泥", "醉酒", "暴怒", "愤怒",
	}
	normalized := cleaned
	for _, sep := range separators[1:] {
		normalized = strings.ReplaceAll(normalized, sep, separators[0])
	}
	parts := strings.Split(normalized, separators[0])
	kept := make([]string, 0, len(parts))
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if part == "" {
			continue
		}
		transient := false
		for _, keyword := range transientKeywords {
			if strings.Contains(part, keyword) {
				transient = true
				break
			}
		}
		if transient {
			continue
		}
		kept = append(kept, part)
	}
	if len(kept) == 0 {
		return cleaned
	}
	joined := strings.Join(kept, separators[0])
	if separators[0] == "。" && !strings.HasSuffix(joined, "。") {
		joined += "。"
	}
	return joined
}
