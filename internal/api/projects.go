func parseEpisodeSceneGenerationResponse(content string) (*EpisodeSceneGenerationResponse, error) {
	jsonContent := cleanupLLMJSON(content)
	var wrapped map[string]interface{}
	if err := json.Unmarshal([]byte(jsonContent), &wrapped); err == nil {
		resp := &EpisodeSceneGenerationResponse{}
		resp.TotalScenes = firstNonZeroInt(readMapInt(wrapped, "total_scenes"), readMapInt(wrapped, "scene_count"))
		if items, ok := wrapped["scenes"].([]interface{}); ok {
			resp.Scenes = normalizeSceneItems(items)
		}
		if items, ok := wrapped["memories"].([]interface{}); ok {
			resp.Memories = normalizeEpisodeSceneMemoryArrayItems(items)
		}
		if items, ok := wrapped["project_anchors"].([]interface{}); ok {
			resp.ProjectAnchors = normalizeProjectAnchorMemoryArrayItems(items)
		} else if items, ok := wrapped["anchors"].([]interface{}); ok {
			resp.ProjectAnchors = normalizeProjectAnchorMemoryArrayItems(items)
		}
		if len(resp.Scenes) > 0 {
			for idx := range resp.Scenes {
				resp.Scenes[idx].normalizeScreenPromptFields()
			}
			if resp.TotalScenes <= 0 {
				resp.TotalScenes = len(resp.Scenes)
			}
			return resp, nil
		}
	}
	var topLevelArray []interface{}
	if err := json.Unmarshal([]byte(jsonContent), &topLevelArray); err == nil {
		resp := &EpisodeSceneGenerationResponse{Scenes: normalizeSceneItems(topLevelArray)}
		resp.TotalScenes = len(resp.Scenes)
		for idx := range resp.Scenes {
			resp.Scenes[idx].normalizeScreenPromptFields()
		}
		if len(resp.Scenes) > 0 {
			return resp, nil
		}
	}
	return nil, fmt.Errorf("unable to parse direct scene response")
}

func extractEpisodeSceneTotalScenesHint(content string) int {
	patterns := []*regexp.Regexp{
		regexp.MustCompile(`"total_scenes"\s*:\s*(\d+)`),
		regexp.MustCompile(`"scene_count"\s*:\s*(\d+)`),
	}
	for _, pattern := range patterns {
		matches := pattern.FindStringSubmatch(content)
		if len(matches) < 2 {
			continue
		}
		totalScenes, err := strconv.Atoi(strings.TrimSpace(matches[1]))
		if err == nil && totalScenes > 0 {
			return totalScenes
		}
	}
	return 0
}

