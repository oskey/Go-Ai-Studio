func summarizeDraftError(cause error) string {
	if cause == nil {
		return ""
	}
	msg := strings.TrimSpace(cause.Error())
	if idx := strings.Index(msg, " Content:"); idx >= 0 {
		msg = strings.TrimSpace(msg[:idx])
	}
	return errfmt.NormalizeUserFacingError(msg, 220)
}
func parseEpisodeRefs(raw string) []int {
	parts := strings.Split(strings.TrimSpace(raw), ",")
	seen := make(map[int]struct{}, len(parts))
	episodes := make([]int, 0, len(parts))
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if part == "" {
			continue
		}
		episode, err := strconv.Atoi(part)
		if err != nil || episode <= 0 {
			continue
		}
		if _, ok := seen[episode]; ok {
			continue
		}
		seen[episode] = struct{}{}
		episodes = append(episodes, episode)
	}
	sort.Ints(episodes)
	return episodes
}
func formatEpisodeRefs(episodes []int) string {
	if len(episodes) == 0 {
		return ""
	}
	parts := make([]string, 0, len(episodes))
	for _, episode := range episodes {
		if episode <= 0 {
			continue
		}
		parts = append(parts, strconv.Itoa(episode))
	}
	return strings.Join(parts, ",")
}
func addEpisodeRef(raw string, episode int) (string, int, int) {
	episodes := parseEpisodeRefs(raw)
	found := false
	for _, existing := range episodes {
		if existing == episode {
			found = true
			break
		}
	}
	if !found && episode > 0 {
		episodes = append(episodes, episode)
		sort.Ints(episodes)
	}
	if len(episodes) == 0 {
		return "", 0, 0
	}
	return formatEpisodeRefs(episodes), episodes[0], episodes[len(episodes)-1]
}
func removeEpisodeRef(raw string, episode int) (string, int, int, int) {
	existing := parseEpisodeRefs(raw)
	if len(existing) == 0 {
		return "", 0, 0, 0
	}
	filtered := make([]int, 0, len(existing))
	for _, item := range existing {
		if item != episode {
			filtered = append(filtered, item)
		}
	}
	if len(filtered) == 0 {
		return "", 0, 0, 0
	}
	return formatEpisodeRefs(filtered), filtered[0], filtered[len(filtered)-1], len(filtered)
}
