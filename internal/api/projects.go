func buildOutlineCoverageCharacters(plot string, availableCharacters []GeneratedCharacter, newlyGeneratedCharacters []GeneratedCharacter, appendMode bool) []GeneratedCharacter {
	if appendMode == false {
		return append([]GeneratedCharacter(nil), availableCharacters...)
	}
	expectedNames := make(map[string]struct{})
	for _, char := range newlyGeneratedCharacters {
		formalName := normalizeGeneratedCharacterName(char.Name)
		if formalName == "" {
			continue
		}
		expectedNames[formalName] = struct{}{}
	}
	formalCharacterNames := buildFormalCharacterNameSet(availableCharacters)
	coverageSource := normalizeSceneTextCharacterNames(extractAppendCoveragePlotBody(plot), availableCharacters)
	for _, name := range extractMentionedFormalCharacters(coverageSource, formalCharacterNames) {
		if name == "" {
			continue
		}
		if appendCoverageRequiresFormalCharacter(coverageSource, name) == false {
			continue
		}
		expectedNames[name] = struct{}{}
	}
	if len(expectedNames) == 0 {
		return append([]GeneratedCharacter(nil), newlyGeneratedCharacters...)
	}
	filtered := make([]GeneratedCharacter, 0, len(expectedNames))
	for _, char := range availableCharacters {
		formalName := normalizeGeneratedCharacterName(char.Name)
		if _, ok := expectedNames[formalName]; ok {
			filtered = append(filtered, char)
		}
	}
	if len(filtered) == 0 {
		return append([]GeneratedCharacter(nil), newlyGeneratedCharacters...)
	}
	return filtered
}
