func estimateNarrationSpeechSeconds(text string) float64 {
	trimmed := strings.TrimSpace(text)
	if trimmed == "" {
		return 0
	}
	seconds := 0.0
	for _, r := range trimmed {
		switch {
		case unicode.Is(unicode.Han, r):
			seconds += 0.28
		case strings.ContainsRune("，。！？；：、,.!?;:", r):
			seconds += 0.22
		case unicode.IsSpace(r):
			seconds += 0
		default:
			seconds += 0.14
		}
	}
	return seconds
}

