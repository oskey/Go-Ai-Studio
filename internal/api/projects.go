func buildGeneratedSceneCharacterBlockingFromPlan(plan EpisodePlanResponse, outline EpisodePlanSceneOutlineItem) string {
	if len(outline.Characters) == 0 {
		return ""
	}
	locationRules, ok := plan.BlockingRules[strings.TrimSpace(outline.LocationID)]
	if !ok || len(locationRules) == 0 {
		return ""
	}
	subset := make(map[string]EpisodePlanBlockingRule, len(outline.Characters))
	for _, characterName := range outline.Characters {
		trimmedName := strings.TrimSpace(characterName)
		if trimmedName == "" {
			continue
		}
		rule, ok := locationRules[trimmedName]
		if !ok {
			continue
		}
		subset[trimmedName] = rule
	}
	if len(subset) == 0 {
		return ""
	}
	return normalizeJSONObjectString(subset)
}

