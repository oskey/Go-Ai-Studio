func loadAppendCharacterRecords(projectID uint, draftProjectID uint) []models.Character {
	candidateIDs := make([]uint, 0, 2)
	seenProjectIDs := make(map[uint]struct{}, 2)
	for _, id := range []uint{projectID, draftProjectID} {
		if id == 0 {
			continue
		}
		if _, ok := seenProjectIDs[id]; ok {
			continue
		}
		seenProjectIDs[id] = struct{}{}
		candidateIDs = append(candidateIDs, id)
	}
	if len(candidateIDs) == 0 {
		return nil
	}
	var chars []models.Character
	db.DB.Where("project_id IN ?", candidateIDs).Order("id asc").Find(&chars)
	merged := make([]models.Character, 0, len(chars))
	seenCharacterKeys := make(map[string]struct{}, len(chars))
	for _, char := range chars {
		key := strings.TrimSpace(char.Name)
		if key == "" {
			key = fmt.Sprintf("%d:%s", char.ProjectID, normalizeGeneratedCharacterName(char.Name))
		}
		if _, ok := seenCharacterKeys[key]; ok {
			continue
		}
		seenCharacterKeys[key] = struct{}{}
		merged = append(merged, char)
	}
	return merged
}
