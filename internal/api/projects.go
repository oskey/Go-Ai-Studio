func loadProjectNextAutoGenerateEpisodes(projectIDs []uint) map[uint]int {
	nextEpisodes := make(map[uint]int, len(projectIDs))
	if len(projectIDs) == 0 {
		return nextEpisodes
	}
	for _, projectID := range projectIDs {
		nextEpisodes[projectID] = 1
	}
	type episodeRow struct {
		ProjectID  uint
		MaxEpisode int
	}
	var rows []episodeRow
	if err := db.DB.Model(&models.Scene{}).
		Select("project_id, COALESCE(MAX(episode), 0) AS max_episode").
		Where("project_id IN ?", projectIDs).
		Group("project_id").
		Scan(&rows).Error; err != nil {
		return nextEpisodes
	}
	for _, row := range rows {
		if row.MaxEpisode > 0 {
			nextEpisodes[row.ProjectID] = row.MaxEpisode + 1
		}
	}
	return nextEpisodes
}

// ListProjects retrieves all projects
func ListProjects(c *gin.Context) {
	var projects []models.Project
	// Preload ArtStyle to show style name in list
	if err := db.DB.Preload("ArtStyle").Order("created_at desc").Find(&projects).Error; err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to fetch projects"})
		return
	}
	if len(projects) > 0 {
		projectIDs := make([]uint, 0, len(projects))
		for _, project := range projects {
			projectIDs = append(projectIDs, project.ID)
		}
		var drafts []models.AutoGenerateDraft
		if err := db.DB.Where("project_id IN ?", projectIDs).Find(&drafts).Error; err == nil {
			draftMap := make(map[uint]models.AutoGenerateDraft, len(drafts))
			for _, draft := range drafts {
				draftMap[draft.ProjectID] = draft
			}
			for idx := range projects {
				if draft, ok := draftMap[projects[idx].ID]; ok {
					projects[idx].HasAutoGenerateDraft = true
					projects[idx].AutoGenerateDraftStage = draft.Stage
				}
			}
		}
		characterCounts := loadProjectRecordCounts[models.Character](projectIDs)
		sceneCounts := loadProjectRecordCounts[models.Scene](projectIDs)
		videoCounts := loadProjectRecordCounts[models.Video](projectIDs)
		nextEpisodes := loadProjectNextAutoGenerateEpisodes(projectIDs)
		for idx := range projects {
			projectID := projects[idx].ID
			projects[idx].IsEmpty = characterCounts[projectID] == 0 && sceneCounts[projectID] == 0 && videoCounts[projectID] == 0
			projects[idx].NextAutoGenerateEpisode = nextEpisodes[projectID]
		}
	}
	c.JSON(http.StatusOK, projects)
}

// AutoGenerateProject handles the automated generation of characters and scenes
func AutoGenerateProject(c *gin.Context) {
	var req models.AutoGenerateRequest
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	req.GenerationMode = normalizeAutoGenerateGenerationMode(req.GenerationMode, req.AllowCharacterSpeech)
	req.TagIDs = normalizeAutoGenerateTagIDs(req.TagIDs)
	req.AllowCharacterSpeech = autoGenerateModeAllowsCharacterSpeech(req.GenerationMode)

	projectID := parseUint(c.Param("id"))
	if projectID == 0 {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid project id"})
		return
	}

	var project models.Project
	if err := db.DB.First(&project, projectID).Error; err != nil {
		c.JSON(http.StatusNotFound, gin.H{"error": "Project not found"})
		return
	}

	if err := setAutoGenerateDraftStage(projectID, req, "queued", "", "", "", 0, ""); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to prepare draft"})
		return
	}

	payload := lightweightStoryTaskPayload{
		ProjectID: projectID,
		Request:   req,
	}
	t, err := task.GlobalTaskManager.AddTask("auto_generate_project", payload)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to submit task"})
		return
	}
	if err := setAutoGenerateDraftCurrentTaskID(projectID, t.ID); err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": "Failed to bind task to draft"})
		return
	}

	c.JSON(http.StatusAccepted, gin.H{"message": "Lightweight story generation task submitted", "task_id": t.ID})
}

