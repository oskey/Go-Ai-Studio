func splitGeneratedScene(scene GeneratedScene, singleMode bool, disableReferenceImages bool, characters []GeneratedCharacter, formalCharacterNames map[string]struct{}) []GeneratedScene {
	resolvedCharacters := filterSceneCharacters(scene.Characters, characters, formalCharacterNames)
	resolvedDialogue := filterSceneDialogue(scene.Dialogue, characters, formalCharacterNames)
	maxCharacters := maxSceneCharactersForDialogue(resolvedDialogue, singleMode, disableReferenceImages)
	speakerGroups := buildDialogueSpeakerGroups(resolvedDialogue, maxDialogueSceneCharactersByMode(singleMode, disableReferenceImages))
	if len(resolvedCharacters) <= maxCharacters && len(speakerGroups) <= 1 {
		scene.Characters = resolvedCharacters
		scene.Dialogue = resolvedDialogue
		return []GeneratedScene{scene}
	}
	if len(speakerGroups) == 0 {
		speakerGroups = buildCharacterWindows(resolvedCharacters, maxCharacters)
	}
	if len(speakerGroups) == 0 {
		scene.Characters = resolvedCharacters
		scene.Dialogue = resolvedDialogue
		return []GeneratedScene{scene}
	}
	fragments := make([]GeneratedScene, 0, len(speakerGroups))
	for idx, group := range speakerGroups {
		fragment := scene
		fragment.Characters = group
		fragment.Dialogue = selectDialogueForGroup(resolvedDialogue, group)
		fragment.Description = addSceneFragmentFocus(focusSceneTextOnGroup(scene.Description, group, characters, "。"), group, idx)
		fragment.Fingerprint = ""
		fragment.Narration = normalizeGeneratedSceneNarration(fragment.Narration, fragment.Dialogue)
		fragments = append(fragments, fragment)
	}
	Log(LogLevelWarn, "自动剧情场景已拆分连续分镜", fmt.Sprintf("原场景: %s, 拆分数量: %d", scene.Name, len(fragments)))
	return fragments
}
func buildDialogueSpeakerGroups(dialogue []string, maxCharacters int) [][]string {
	if len(dialogue) == 0 || maxCharacters <= 0 {
		return nil
	}
	var groups [][]string
	current := make([]string, 0, maxCharacters)
	currentSet := make(map[string]struct{}, maxCharacters)
	lastSpeaker := ""
	for _, line := range dialogue {
		speaker := extractDialogueSpeaker(line)
		if speaker == "" {
			continue
		}
		if _, ok := currentSet[speaker]; ok {
			lastSpeaker = speaker
			continue
		}
		if len(currentSet) < maxCharacters {
			current = append(current, speaker)
			currentSet[speaker] = struct{}{}
			lastSpeaker = speaker
			continue
		}
		if len(current) > 0 {
			groups = append(groups, append([]string(nil), current...))
		}
		current = make([]string, 0, maxCharacters)
		currentSet = make(map[string]struct{}, maxCharacters)
		if maxCharacters > 1 && lastSpeaker != "" && lastSpeaker != speaker {
			current = append(current, lastSpeaker, speaker)
			currentSet[lastSpeaker] = struct{}{}
			currentSet[speaker] = struct{}{}
		} else {
			current = append(current, speaker)
			currentSet[speaker] = struct{}{}
		}
		lastSpeaker = speaker
	}
	if len(current) > 0 {
		groups = append(groups, append([]string(nil), current...))
	}
	return dedupeCharacterGroups(groups)
}
func buildCharacterWindows(characters []string, maxCharacters int) [][]string {
	if len(characters) == 0 || maxCharacters <= 0 {
		return nil
	}
	if len(characters) <= maxCharacters {
		return [][]string{append([]string(nil), characters...)}
	}
	var groups [][]string
	for start := 0; start < len(characters); start++ {
		end := start + maxCharacters
		if end > len(characters) {
			end = len(characters)
		}
		group := append([]string(nil), characters[start:end]...)
		groups = append(groups, group)
		if end == len(characters) {
			break
		}
	}
	return dedupeCharacterGroups(groups)
}
func dedupeCharacterGroups(groups [][]string) [][]string {
	seen := make(map[string]struct{}, len(groups))
	filtered := make([][]string, 0, len(groups))
	for _, group := range groups {
		key := strings.Join(group, "|")
		if key == "" {
			continue
		}
		if _, ok := seen[key]; ok {
			continue
		}
		seen[key] = struct{}{}
		filtered = append(filtered, group)
	}
	return filtered
}
