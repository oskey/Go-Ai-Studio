func sanitizeGeneratedScenePromptCharacterNames(text string, characters []GeneratedCharacter) string {
	output := strings.TrimSpace(text)
	if output == "" || len(characters) == 0 {
		return output
	}

	type replacement struct {
		name  string
		alias string
	}

	replacements := make([]replacement, 0, len(characters))
	seen := make(map[string]struct{}, len(characters))
	for _, char := range characters {
		alias := buildGeneratedCharacterPromptAlias(char)
		candidate := strings.TrimSpace(char.Name)
		if candidate == "" {
			continue
		}
		if _, ok := seen[candidate]; ok {
			continue
		}
		seen[candidate] = struct{}{}
		replacements = append(replacements, replacement{name: candidate, alias: alias})
	}

	sort.SliceStable(replacements, func(i, j int) bool {
		return utf8.RuneCountInString(replacements[i].name) > utf8.RuneCountInString(replacements[j].name)
	})

	for _, item := range replacements {
		output = strings.ReplaceAll(output, item.name, item.alias)
	}
	return output
}
