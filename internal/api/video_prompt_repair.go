func validateSingleShotSceneRepairResponse(payload *singleShotSceneRepairResponse) error {
	if payload == nil {
		return fmt.Errorf("repair payload is nil")
	}
	if payload.DurationSeconds < 3 {
		return fmt.Errorf("duration_seconds must be at least 3")
	}
	if payload.ImagePrompt == "" {
		return fmt.Errorf("image_prompt is required")
	}
	if payload.VideoPrompt == "" {
		return fmt.Errorf("video_prompt is required")
	}
	for _, label := range []string{"主体：", "场景：", "构图：", "光影：", "约束："} {
		if !strings.Contains(payload.ImagePrompt, label) {
			return fmt.Errorf("image_prompt must include %s", label)
		}
	}
	if _, err := parseFlowingVideoPrompt(payload.VideoPrompt); err != nil {
		return err
	}
	return nil
}

