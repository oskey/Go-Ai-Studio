var storyboardSectionHeaderPattern = regexp.MustCompile(`^\s*分镜\s*(\d+)\s*$`)

const (
	singleShotRepairTargetScene = "scene"
	singleShotRepairTargetVideo = "video"
)

func normalizeSingleShotRepairTarget(target string) string {
	switch strings.ToLower(strings.TrimSpace(target)) {
	case singleShotRepairTargetVideo:
		return singleShotRepairTargetVideo
	default:
		return singleShotRepairTargetScene
	}
}

func loadSingleShotRepairContext(projectID uint, sceneID uint, videoID uint) (*singleShotRepairContext, error) {
	var project models.Project
	if err := db.DB.First(&project, projectID).Error; err != nil {
		return nil, fmt.Errorf("project not found")
	}

	scene, err := loadSceneViewByShotID(sceneID, true)
	if err != nil {
		return nil, fmt.Errorf("scene not found")
	}
	if scene.ProjectID != projectID {
		return nil, fmt.Errorf("scene does not belong to current project")
	}

	targetVideoID := videoID
	if targetVideoID == 0 {
		targetVideoID = sceneID
	}

	var video models.Video
	if err := db.DB.First(&video, targetVideoID).Error; err != nil {
		return nil, fmt.Errorf("video not found")
	}
	if video.ProjectID != projectID {
		return nil, fmt.Errorf("video does not belong to current project")
	}
	if err := hydrateVideoScene(&video, false); err != nil {
		return nil, err
	}

	if strings.TrimSpace(scene.ImagePrompt) == "" {
		return nil, fmt.Errorf("current scene is missing image_prompt")
	}
	if strings.TrimSpace(video.VideoPrompt) == "" && strings.TrimSpace(scene.VideoPrompt) == "" {
		return nil, fmt.Errorf("current shot is missing video_prompt")
	}

	prevScene, _ := findRepairNeighborScene(projectID, scene.Episode, scene.SceneNumber, -1)
	nextScene, _ := findRepairNeighborScene(projectID, scene.Episode, scene.SceneNumber, 1)

	var draft models.AutoGenerateDraft
	var draftPtr *models.AutoGenerateDraft
	if err := db.DB.Where("project_id = ?", projectID).First(&draft).Error; err == nil {
		draftPtr = &draft
	}

	return &singleShotRepairContext{
		Project:           project,
		Scene:             scene,
		Video:             video,
		PreviousScene:     prevScene,
		NextScene:         nextScene,
		Draft:             draftPtr,
		OriginalPlotBlock: buildRepairPlotContext(draftPtr, scene.SceneNumber),
		EpisodeScenesJSON: buildRepairEpisodeScenesJSON(projectID, scene.Episode),
	}, nil
}

func findRepairNeighborScene(projectID uint, episode int, currentSceneNumber int, direction int) (*models.Scene, error) {
	query := db.DB.Preload("Characters").Where("project_id = ? AND episode = ?", projectID, episode)
	if direction < 0 {
		query = query.Where("scene_number < ?", currentSceneNumber).Order("scene_number desc").Limit(1)
	} else {
		query = query.Where("scene_number > ?", currentSceneNumber).Order("scene_number asc").Limit(1)
	}

	var scene models.Scene
	if err := query.First(&scene).Error; err != nil {
		return nil, err
	}
	return &scene, nil
}

func extractPromptSubjectLine(prompt string) string {
	trimmed := strings.TrimSpace(prompt)
	if trimmed == "" {
		return ""
	}
	for _, line := range strings.Split(trimmed, "\n") {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, "主体：") {
			return strings.TrimSpace(strings.TrimPrefix(line, "主体："))
		}
	}
	return ""
}

func sceneContainsCharacter(scene models.Scene, characterID uint) bool {
	for _, char := range scene.Characters {
		if char.ID == characterID {
			return true
		}
	}
	return false
}

func buildRepairCharacterPreviewPrompt(char models.Character) string {
	if text := strings.TrimSpace(char.PositivePrompt); text != "" {
		return trimToRuneLimit(text, 420)
	}
	text, err := buildCharacterPreviewPositivePrompt(char)
	if err != nil {
		return ""
	}
	return trimToRuneLimit(text, 420)
}

func buildRepairCharacterContext(scene models.Scene, prev *models.Scene, next *models.Scene) string {
	if len(scene.Characters) == 0 {
		return "当前镜头没有绑定角色，优先根据当前 image_prompt、video_prompt、原始剧本片段和前后邻镜判断连续性。"
	}

	currentSubject := extractPromptSubjectLine(scene.ImagePrompt)
	prevSubject := ""
	if prev != nil {
		prevSubject = extractPromptSubjectLine(prev.ImagePrompt)
	}
	nextSubject := ""
	if next != nil {
		nextSubject = extractPromptSubjectLine(next.ImagePrompt)
	}

	sections := make([]string, 0, len(scene.Characters))
	for _, char := range scene.Characters {
		identityParts := make([]string, 0, 3)
		if text := strings.TrimSpace(char.Appearance); text != "" {
			identityParts = append(identityParts, text)
		}
		if text := strings.TrimSpace(char.Description); text != "" && text != strings.TrimSpace(char.Appearance) {
			identityParts = append(identityParts, text)
		}
		if text := strings.TrimSpace(char.Fingerprint); text != "" {
			identityParts = append(identityParts, text)
		}

		lines := []string{
			fmt.Sprintf("- 角色：%s", strings.TrimSpace(char.Name)),
			fmt.Sprintf("  固定身份锚点：%s", strings.TrimSpace(strings.Join(identityParts, "；"))),
		}
		if previewPrompt := buildRepairCharacterPreviewPrompt(char); previewPrompt != "" {
			lines = append(lines, fmt.Sprintf("  角色预览提示词：%s", previewPrompt))
		}
		if currentSubject != "" {
			lines = append(lines, fmt.Sprintf("  当前镜头主体行：%s", currentSubject))
		}
		if prev != nil && sceneContainsCharacter(*prev, char.ID) && prevSubject != "" {
			lines = append(lines, fmt.Sprintf("  上一镜主体行：%s", prevSubject))
		}
		if next != nil && sceneContainsCharacter(*next, char.ID) && nextSubject != "" {
			lines = append(lines, fmt.Sprintf("  下一镜主体行：%s", nextSubject))
		}
		sections = append(sections, strings.Join(lines, "\n"))
	}

	return strings.Join(sections, "\n\n")
}

func summarizeRepairNeighborScene(label string, scene *models.Scene) string {
	if scene == nil {
		return fmt.Sprintf("%s：无", label)
	}

	videoPrompt := strings.TrimSpace(scene.VideoPrompt)
	if videoPrompt == "" {
		videoPrompt = "（无）"
	}
	description := strings.TrimSpace(scene.Description)
	if description == "" {
		description = "（无）"
	}
	imagePrompt := strings.TrimSpace(scene.ImagePrompt)
	if imagePrompt == "" {
		imagePrompt = "（无）"
	}

	return fmt.Sprintf(`%s：
- scene_number: %d
- duration_seconds: %d
- description: %s
- image_prompt:
%s
- video_prompt:
%s`, label, scene.SceneNumber, scene.DurationSeconds, description, imagePrompt, videoPrompt)
}

func buildRepairPlotContext(draft *models.AutoGenerateDraft, currentSceneNumber int) string {
	if draft == nil {
		return ""
	}
	plot := strings.TrimSpace(draft.Plot)
	if plot == "" {
		return ""
	}

	if currentSceneNumber <= 0 {
		return trimToRuneLimit(plot, 1800)
	}

	lines := strings.Split(plot, "\n")
	type section struct {
		number int
		start  int
		end    int
	}
	sections := make([]section, 0)
	for idx, line := range lines {
		matches := storyboardSectionHeaderPattern.FindStringSubmatch(strings.TrimSpace(line))
		if len(matches) != 2 {
			continue
		}
		number := 0
		fmt.Sscanf(matches[1], "%d", &number)
		if number <= 0 {
			continue
		}
		sections = append(sections, section{number: number, start: idx})
	}

	if len(sections) == 0 {
		return trimToRuneLimit(plot, 1800)
	}
	for i := range sections {
		if i+1 < len(sections) {
			sections[i].end = sections[i+1].start
		} else {
			sections[i].end = len(lines)
		}
	}

	targets := map[int]string{
		currentSceneNumber - 1: "上一段原始分镜",
		currentSceneNumber:     "当前原始分镜",
		currentSceneNumber + 1: "下一段原始分镜",
	}
	blocks := make([]string, 0, 3)
	for _, sec := range sections {
		label, ok := targets[sec.number]
		if !ok {
			continue
		}
		text := strings.TrimSpace(strings.Join(lines[sec.start:sec.end], "\n"))
		if text == "" {
			continue
		}
		blocks = append(blocks, fmt.Sprintf("%s：\n%s", label, text))
	}
	if len(blocks) == 0 {
		return trimToRuneLimit(plot, 1800)
	}
	return strings.Join(blocks, "\n\n")
}

func buildRepairEpisodeScenesJSON(projectID uint, episode int) string {
	if projectID == 0 || episode <= 0 {
		return ""
	}

	type repairEpisodeSceneItem struct {
		SceneNumber     int      `json:"scene_number"`
		Name            string   `json:"name,omitempty"`
		DurationSeconds int      `json:"duration_seconds,omitempty"`
		Description     string   `json:"description,omitempty"`
		Narration       string   `json:"narration,omitempty"`
		CharacterNames  []string `json:"character_names,omitempty"`
		ImagePrompt     string   `json:"image_prompt,omitempty"`
		VideoPrompt     string   `json:"video_prompt,omitempty"`
	}

	var scenes []models.Scene
	if err := db.DB.Preload("Characters").
		Where("project_id = ? AND episode = ?", projectID, episode).
		Order("scene_number asc").
		Find(&scenes).Error; err != nil {
		return ""
	}
	if len(scenes) == 0 {
		return ""
	}

	items := make([]repairEpisodeSceneItem, 0, len(scenes))
	for _, scene := range scenes {
		item := repairEpisodeSceneItem{
			SceneNumber:     scene.SceneNumber,
			Name:            strings.TrimSpace(scene.Name),
			DurationSeconds: scene.DurationSeconds,
			Description:     strings.TrimSpace(scene.Description),
			Narration:       strings.TrimSpace(scene.Narration),
			ImagePrompt:     strings.TrimSpace(scene.ImagePrompt),
			VideoPrompt:     strings.TrimSpace(scene.VideoPrompt),
		}
		if len(scene.Characters) > 0 {
			names := make([]string, 0, len(scene.Characters))
			for _, char := range scene.Characters {
				if name := strings.TrimSpace(char.Name); name != "" {
					names = append(names, name)
				}
			}
			if len(names) > 0 {
				item.CharacterNames = names
			}
		}
		items = append(items, item)
	}

	raw, err := json.MarshalIndent(items, "", "  ")
	if err != nil {
		return ""
	}
	return string(raw)
}

func trimToRuneLimit(text string, limit int) string {
	trimmed := strings.TrimSpace(text)
	if limit <= 0 {
		return trimmed
	}
	runes := []rune(trimmed)
	if len(runes) <= limit {
		return trimmed
	}
	return strings.TrimSpace(string(runes[:limit])) + "……"
}

