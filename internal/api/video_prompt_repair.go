func persistSingleShotSceneRepair(projectID uint, sceneID uint, videoID uint, payload *singleShotSceneRepairResponse) error {
	if payload == nil {
		return fmt.Errorf("repair payload is nil")
	}

	return db.RetryOnBusy(func() error {
		return db.DB.Transaction(func(tx *gorm.DB) error {
			var scene models.Scene
			if err := tx.First(&scene, sceneID).Error; err != nil {
				return fmt.Errorf("scene not found")
			}
			if scene.ProjectID != projectID {
				return fmt.Errorf("scene does not belong to current project")
			}

			videoFingerprint, err := buildLegacyVideoFingerprintFromPrompt(payload.VideoPrompt, payload.DurationSeconds)
			if err != nil {
				return err
			}

			scene.ImagePrompt = payload.ImagePrompt
			scene.VideoPrompt = payload.VideoPrompt
			scene.DurationSeconds = payload.DurationSeconds
			scene.PositivePrompt = marshalLocalizedPromptText(payload.ImagePrompt, "")
			scene.VideoFingerprint = videoFingerprint
			scene.UpdatedAt = time.Now()
			if err := tx.Save(&scene).Error; err != nil {
				return err
			}

			targetVideoID := videoID
			if targetVideoID == 0 {
				targetVideoID = sceneID
			}

			var video models.Video
			if err := tx.First(&video, targetVideoID).Error; err != nil {
				return fmt.Errorf("video not found")
			}
			if video.ProjectID != projectID {
				return fmt.Errorf("video does not belong to current project")
			}

			if err := removeGeneratedAsset(scene.GeneratedImage); err != nil {
				return err
			}
			if err := removeGeneratedVideoAsset(video.GeneratedVideo); err != nil {
				return err
			}
			var segments []models.VideoSegment
			if err := tx.Where("video_id = ?", video.ID).Find(&segments).Error; err != nil {
				return err
			}
			for _, segment := range segments {
				if err := removeGeneratedVideoAsset(segment.GeneratedVideo); err != nil {
					return err
				}
				if err := removeGeneratedVideoAsset(segment.TransitionFrame); err != nil {
					return err
				}
			}
			if err := tx.Where("video_id = ?", video.ID).Delete(&models.VideoSegment{}).Error; err != nil {
				return err
			}

			video.VideoPrompt = payload.VideoPrompt
			video.VideoFingerprint = videoFingerprint
			video.DurationSeconds = payload.DurationSeconds
			video.PositivePrompt = ""
			video.NegativePrompt = getFixedLTXVideoNegativePromptEN()
			video.JMTaskID = ""
			video.GeneratedVideo = ""
			video.GeneratedWorkflow = ""
			video.Status = "draft"
			video.UpdatedAt = time.Now()
			if err := tx.Save(&video).Error; err != nil {
				return err
			}

			scene.GeneratedImage = ""
			scene.GeneratedWorkflow = ""
			scene.Status = "draft"
			scene.UpdatedAt = time.Now()
			if err := tx.Save(&scene).Error; err != nil {
				return err
			}

			return nil
		})
	})
}

