func HandleRepairSceneVideoPromptsTask(t *models.Task) (interface{}, error) {
	var payload singleShotRepairTaskPayload
	if err := json.Unmarshal([]byte(t.Payload), &payload); err != nil {
		return nil, fmt.Errorf("invalid payload: %v", err)
	}
	targetType := normalizeSingleShotRepairTarget(payload.TargetType)

	task.GlobalTaskManager.UpdateTaskProgress(t.ID, 10, "读取当前镜头数据")
	ctx, err := loadSingleShotRepairContext(payload.ProjectID, payload.SceneID, payload.VideoID)
	if err != nil {
		return nil, err
	}

	task.GlobalTaskManager.UpdateTaskProgress(t.ID, 25, "构造单镜修复请求")
	systemPrompt, userPrompt, err := buildSingleShotRepairPrompts(ctx, payload.Reason, targetType)
	if err != nil {
		return nil, err
	}

	var provider models.LLMProvider
	if err := db.DB.Where("is_active = ?", true).First(&provider).Error; err != nil {
		return nil, fmt.Errorf("no active LLM provider found")
	}

	Log(LogLevelInfo, llmLogMessage("LLM Request", provider), fmt.Sprintf("Starting single-shot repair for project=%d scene=%d video=%d", payload.ProjectID, payload.SceneID, payload.VideoID))
	Log(LogLevelInfo, llmLogMessage("LLM Request Prompt", provider), fmt.Sprintf("System:\n%s\n\nUser:\n%s", systemPrompt, userPrompt))

	task.GlobalTaskManager.UpdateTaskProgress(t.ID, 55, "正在调用 LLM 修复当前镜头提示词")
	raw, err := requestSingleShotRepairOnce(provider, systemPrompt, userPrompt, t.ID)
	if err != nil {
		Log(LogLevelError, llmLogMessage("LLM Error", provider), fmt.Sprintf("Single-shot repair failed: %v", err))
		return nil, err
	}

	Log(LogLevelInfo, llmLogMessage("LLM 完整返回(单镜提示词修复)", provider), raw)

	task.GlobalTaskManager.UpdateTaskProgress(t.ID, 78, "校验修复结果")
	if targetType == singleShotRepairTargetVideo {
		repairResponse, err := parseSingleShotVideoRepairResponse(raw)
		if err != nil {
			Log(LogLevelError, llmLogMessage("LLM 返回解析失败(单镜提示词修复)", provider), err.Error())
			return nil, err
		}
		if err := validateSingleShotVideoRepairResponse(repairResponse); err != nil {
			Log(LogLevelError, llmLogMessage("LLM 返回校验失败(单镜提示词修复)", provider), err.Error())
			return nil, err
		}

		task.GlobalTaskManager.UpdateTaskProgress(t.ID, 92, "回写当前视频提示词")
		if err := persistSingleShotVideoRepair(payload.ProjectID, payload.VideoID, repairResponse); err != nil {
			return nil, err
		}

		BroadcastUpdate("scene", payload.SceneID)
		BroadcastUpdate("video", payload.VideoID)

		task.GlobalTaskManager.UpdateTaskProgress(t.ID, 100, "视频提示词已修复")
		return gin.H{
			"target_type":  targetType,
			"scene_id":     payload.SceneID,
			"video_id":     payload.VideoID,
			"video_prompt": repairResponse.VideoPrompt,
		}, nil
	}

	repairResponse, err := parseSingleShotSceneRepairResponse(raw)
	if err != nil {
		Log(LogLevelError, llmLogMessage("LLM 返回解析失败(单镜提示词修复)", provider), err.Error())
		return nil, err
	}
	if err := validateSingleShotSceneRepairResponse(repairResponse); err != nil {
		Log(LogLevelError, llmLogMessage("LLM 返回校验失败(单镜提示词修复)", provider), err.Error())
		return nil, err
	}

	task.GlobalTaskManager.UpdateTaskProgress(t.ID, 92, "回写当前镜头提示词")
	if err := persistSingleShotSceneRepair(payload.ProjectID, payload.SceneID, payload.VideoID, repairResponse); err != nil {
		return nil, err
	}

	BroadcastUpdate("scene", payload.SceneID)
	BroadcastUpdate("video", payload.VideoID)

	task.GlobalTaskManager.UpdateTaskProgress(t.ID, 100, "镜头提示词已修复")
	return gin.H{
		"target_type":      targetType,
		"scene_id":         payload.SceneID,
		"video_id":         payload.VideoID,
		"duration_seconds": repairResponse.DurationSeconds,
		"image_prompt":     repairResponse.ImagePrompt,
		"video_prompt":     repairResponse.VideoPrompt,
	}, nil
}

func parseUintParam(value string) (uint, error) {
	trimmed := strings.TrimSpace(value)
	if trimmed == "" {
		return 0, fmt.Errorf("empty numeric parameter")
	}
	var parsed uint64
	_, err := fmt.Sscanf(trimmed, "%d", &parsed)
	if err != nil {
		return 0, err
	}
	return uint(parsed), nil
}
