func buildCharacterContinuityLedgerRule() string {
	return "在开始生成 scenes 之前，先静默建立每个主要角色的“当前连续状态账本”，并在后续镜头持续继承：固定身份块（国别或文化、性别、明确年龄、明确身高、发型、发际线、脸型、眉眼、鼻唇、嘴周与下巴状态、独特识别点）、当前服装块（按贴身层 -> 中间层 -> 外层顺序固定颜色、材质、版型、长度与层级）、当前损伤块（破损、血迹、包扎、伤口位置）、当前污渍与湿度块、当前手持物块和当前站位关系。若角色已被建立为年轻、年轻成年或少年男性，不要主动添加胡须、胡茬、络腮胡、八字胡等描述；若当前机位看见嘴周，只写嘴周干净、上唇干净、下巴干净等无胡须状态即可。只有当脚本、角色外观或前文已明确建立中年、年长或固定胡须样式时，才允许写出并持续继承胡须状态。若当前镜头只露出手、前臂、肩背、腰侧、局部侧脸或局部轮廓，也必须继续带上当前仍可见的服装颜色、材质、袖口、外套层次、身形比例和手持物，确保观众能看出这仍然是谁，而不是只剩一只无身份的手或一块没有归属的轮廓。除非剧情或脚本明确写出变化，禁止换脸、禁止换发型、禁止换衣服、禁止换颜色、禁止重置破损状态、禁止更换道具、禁止重置角色状态。"
}

func buildVisibleAnchorReuseRule() string {
	return "输出单个 scene 时，不要把整套连续状态重新发明，也不要把它压缩成“东亚男性24岁180cm下巴干净无胡须”这种低信息短串。你必须先判断当前镜头真正看得见什么，然后只输出当前可见部分；但所有可见部分都必须沿用最近一次已经建立的原词与顺序，不能自由换词、换层级或改写成同义词。正面或近正面镜头必须完整复用当前可见的脸部与发型锚点；半身或更近镜头必须同时完整复用当前可见的服装层次、颜色、材质、破损和污渍；侧脸、背身、遮挡或远景镜头可以删掉不可见细节，但仍要完整复用当前可见的头脸轮廓、发型结构、服装层次、手持物和状态痕迹。不要用杯子、书本、手机、纸张、酒杯、武器或其他手持道具遮挡当前需要稳定露脸的人物脸部；凡是当前镜头需要承担后续连续表演、口型、反应、主位动作或关键联动的人物，首帧都必须把眉眼、鼻梁、嘴周和下颌这些关键识别区直接露出来。主位人物或主角若当前镜头需要露脸，首帧必须至少建立半张以上可见脸部与稳定发型锚点；只露一点点侧脸、只剩脸部边缘轮廓或被前景物挡住半张以上脸，只允许留给非主角、非主位或背景人物。若当前镜头的主角或主位人物不能完整露脸，就必须额外补足当前可见的服装颜色、材质、袖口、外套层次、体型比例、发型结构和手持物，避免 z-image 与 LTX2.3 把成年人误补成儿童、把熟悉角色误补成陌生脸。若某个角色在后续视频里要与他人发生直接联动、对打、递交、拉扯、靠近、说话、阻挡或受击，这个角色就必须先在首帧中以可识别方式出现；不要只留一只手、一点肩膀或一点侧脸，再让 LTX2.3 自己补完整个人。年轻、年轻成年或少年男性不要主动添加胡须或胡茬描述；只有当脚本、角色外观或前文已经明确建立中年、年长或固定胡须样式时，才写并持续继承胡须状态。"
}

func buildCurrentVisibleStateCarryRule() string {
	return "主体行里的当前镜头状态必须完整继承最近一次已经建立且当前仍可见的服装层次、颜色、材质、破损、血迹、湿度、包扎、手持物和站位关系；除非剧情或脚本明确写出变化，禁止重置、禁止改色、禁止换层、禁止换武器、禁止清空破损状态。"
}

func buildVisibleSceneContinuityRule() string {
	return "同一地点或同一场景的连贯性，必须靠当前镜头里仍然可见的环境锚点重新写出来，而不是只写“仍在同一地点”“还在同一场景”这类抽象续写。凡是当前机位仍然可见的门框、窗格、柱子、桌案、床沿、船舷、栏杆、红绸、喜烛、灯台、墙面、地面纹理、主光方向、阴影方向和主要道具位置，都必须沿用最近一次已经建立的原词、材质、颜色、相对位置和空间层级继续复用；不在当前机位可见范围内的可以删去，但除非剧情或脚本明确写出变化，禁止换成另一套空间。若空间、道具位置或光线真的发生变化，必须把变化本身写成当前镜头可见的事实。"
}

func buildReadableNarrationRule() string {
	return "narration 必须先点明当前画面里是谁或什么在出镜，再概括这一镜正在发生什么和这一镜的信息落点；不要只写空泛氛围句。若当前镜头是人物镜头，必须直接点名人物姓名；若当前镜头只拍手、剑、令牌、伤口、尸体、针、脚步或其他局部特写，也必须点明这是“谁的手”“谁掌中的令牌”“谁肩头的伤口”“哪些人的尸体”，让人一眼就能看懂这镜在讲谁和讲什么。"
}
